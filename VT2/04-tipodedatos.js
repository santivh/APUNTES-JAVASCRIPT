// Tipo de datos en JS
// typeof es un operador de JS que nos devuelve una cadena de texto indicando el tipo de dato de una variable
// Emplearemos console.log para que visualizar el tipo de datos por consola (F12)

console.log(typeof "Hola");
console.log(typeof (42));
console.log(typeof (16,9));
console.log(typeof(true));

var a;
console.log(typeof(a));

var b = null;
console.log(typeof(b));

console.log(typeof(Math));

console.log(b == a);
console.log(b === a);